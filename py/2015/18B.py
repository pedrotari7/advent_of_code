def update_states(c, s, i, j):

	n = []

	if i-1>=0:
		n.append(s[i-1][j]) 
		if j-1>=0:
			n.append(s[i-1][j-1])
		if j+1<len(s):
			n.append(s[i-1][j+1])	
	if j-1>=0:
		n.append(s[i][j-1])
		if i+1<len(s):
			n.append(s[i+1][j-1])
	if j+1<len(s):
		n.append(s[i][j+1])
	if i+1<len(s):
		n.append(s[i+1][j])
		if j+1<len(s):
			n.append(s[i+1][j+1])

	if (i,j) not in [(0,0),(0,len(s)-1),(len(s)-1,0),(len(s)-1,len(s)-1)] :
		if s[i][j] == '.':
			c[i][j] = '#' if n.count('#') == 3 else '.'
		elif s[i][j] == '#':
				c[i][j] = '#' if n.count('#') in [2,3] else '.'
	return c



